    {
        $peminjaman = Peminjaman::join('inventory_item_detail', 'inventory_lending.id_item', '=', 'inventory_item_detail.id')
            ->join('inventory_item', 'inventory_item_detail.code', '=', 'inventory_item.code')
            ->where('inventory_lending.id', $id)
            ->select('inventory_item_detail.*', 'inventory_item.*','inventory_lending.*')
            ->first();
        return view('pages/peminjaman/detail_peminjaman')->with([
            'peminjaman' => $peminjaman,
        ]);
    }

